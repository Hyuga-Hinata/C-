#include<stdio.h>
int main(void)
{
	double a[45][5][5]={0};
	int i,j,l;
	a[0][1][1]=a[0][2][1]=a[0][3][1]=25;
	a[0][1][2]=a[0][2][2]=a[0][3][2]=50;
	a[0][1][3]=a[0][2][3]=a[0][3][3]=75;
	for(i=0;i<40;i++)
	a[i][1][4]=a[i][2][4]=a[i][3][4]=100;
	for(i=1;i<40;i++)
	{
		for(j=1;j<=3;j++)
		{
			for(l=1;l<=3;l++)
			{
				a[i][j][l]=0.25*(a[i-1][j-1][l]+a[i-1][j][l-1]+a[i-1][j+1][l]+a[i-1][j][l+1]);
			}
		}
		if((a[i][1][1]-a[i-1][1][1])<0.001&&(a[i][1][1]-a[i-1][1][1])>-0.001&&(a[i][1][2]-a[i-1][1][2])<0.001&&(a[i][1][2]-a[i-1][1][2])>-0.001&&(a[i][1][3]-a[i-1][1][3])<0.001&&(a[i][1][3]-a[i-1][1][3])>-0.001&&(a[i][2][1]-a[i-1][2][1])<0.001&&(a[i][2][1]-a[i-1][2][1])>-0.001&&(a[i][2][2]-a[i-1][2][2])<0.001&&(a[i][2][2]-a[i-1][2][2])>-0.001&&(a[i][2][3]-a[i-1][2][3])>-0.001
		&&(a[i][3][1]-a[i-1][3][1])<0.001&&(a[i][3][1]-a[i-1][3][1])>-0.001&&(a[i][3][2]-a[i-1][3][2])<0.001&&(a[i][3][2]-a[i-1][3][2])>-0.001&&(a[i][3][3]-a[i-1][3][3])<0.001&&(a[i][3][3]-a[i-1][3][3])>-0.001)
			break;
	}
	
	for(j=1;j<=3;j++)
	{
		for(l=1;l<=3;l++)
		{
			printf("%lf  ",a[i][j][l]);
		}
		printf("\n");
	}
	return 0;
}
